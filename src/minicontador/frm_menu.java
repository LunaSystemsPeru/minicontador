    private void txt_contra_loginKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_contra_loginKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txt_contra_login.getText().length() > 3) {
                String pass = String.valueOf(txt_contra_login.getPassword());
                if (pass.equals(c_usuario.getPassword())) {
                    //cargar empresas
                    m_empresas m_empresas = new m_empresas();
                    m_empresas.listar_empresas(cbx_empresas);
                    cbx_empresas.setEnabled(true);
                    cbx_empresas.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(frm_login, "Contraseña Incorrecta..");
                    txt_contra_login.selectAll();
                    txt_contra_login.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(frm_login, "Ingrese su contraseña");
                txt_contra_login.selectAll();
                txt_contra_login.requestFocus();
            }
        }
    }//GEN-LAST:event_txt_contra_loginKeyPressed

    private void txt_ruc_regKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_ruc_regKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txt_ruc_reg.getText().length() == 11) {
                String ruc = txt_ruc_reg.getText().trim();
                try {
                    String json_entidad = cl_json_entidad.getJSONRUC(ruc);
                    String[] datos_entidad = cl_json_entidad.showJSONRUC(json_entidad);
                    txt_razon_reg.setText(datos_entidad[0].trim());
                    c_entidad.setDocumento(ruc);
                    c_entidad.setDireccion(datos_entidad[1].trim());
                    c_entidad.setNombre(datos_entidad[0].trim());
                    btn_add_reg.setEnabled(true);
                    btn_add_reg.requestFocus();
                } catch (ParseException e) {
                    JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
                }
            } else {
                JOptionPane.showMessageDialog(frm_login, "Ingrese un numero correcto, 11 digitos");
                txt_contra_login.selectAll();
                txt_contra_login.requestFocus();
            }
        }
    }//GEN-LAST:event_txt_ruc_regKeyPressed

