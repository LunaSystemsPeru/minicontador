    private void btn_add_regActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_regActionPerformed
        c_conectar.conectar();

        c_usuario.setNombres(txt_nombres_reg.getText().trim().toUpperCase());
        c_usuario.setCelular(txt_celular_reg.getText());
        c_usuario.setEmail(txt_email_reg.getText());
        boolean existe_email = c_usuario.validar_email();
        if (!existe_email) {
            String password = String.valueOf(txt_pass_reg.getPassword());
            String valida_pass = String.valueOf(txt_valida_pass.getPassword());
            if (password.equals(valida_pass)) {
                c_usuario.setPassword(password);
            } else {
                JOptionPane.showMessageDialog(null, "CONTRASEÑA NO COINCIDEN");
                txt_pass_reg.requestFocus();
            }

            c_usuario.obtener_id();

            boolean registrado = c_usuario.insertar();

            if (registrado) {
                c_entidad.setId_usuario(c_usuario.getId_usuario());
                c_entidad.obtener_id();
                boolean registrar_entidad = c_entidad.insertar();

                if (registrar_entidad) {
                    c_empresa.setId_usuario(c_usuario.getId_usuario());
                    c_empresa.setId_entidad(c_entidad.getId_entidad());
                    c_empresa.obtener_id();

                    if (c_empresa.insertar()) {
                        cl_entidad c_entidad_0 = new cl_entidad();
                        c_entidad_0.setDocumento("0");
                        c_entidad_0.setDireccion("-");
                        c_entidad_0.setId_usuario(c_usuario.getId_usuario());
                        c_entidad_0.setNombre("EMPRESA NO IDENTIFICADA");
                        c_entidad_0.setId_entidad(0);
                        c_entidad_0.insertar();

                        cl_PeticionPost c_peticion = new cl_PeticionPost();

                        String mensaje = "USUARIO CREADO EN MINICONTADOR!!. Para acceder su email es: " + c_usuario.getEmail() + " y su contraseña es: " + c_usuario.getPassword() + ". GRACIAS. Atte. LunaSystemsPeru";
                        c_peticion.excutePost(mensaje, c_usuario.getCelular());
                        //cerrar formulario registro
                        frm_registrar.dispose();
                    }
                }
                //enviar sms con acceso 

            }
        } else {
            JOptionPane.showMessageDialog(frm_registrar, "EL EMAIL YA ESTA REGISTRADO, POR FAVOR COMUNIQUESE CON NOSOTROS.");
            txt_email_reg.requestFocus();
        }

    }//GEN-LAST:event_btn_add_regActionPerformed

