    private void txt_proveedorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_proveedorKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txt_proveedor.getText().length() == 11) {
                //validar si existe
                String ruc = txt_proveedor.getText().trim();
                
                c_entidad.setDocumento(ruc);
                c_entidad.setId_usuario(id_usuario);
                boolean existe = c_entidad.validar_documento();
                if (existe) {
                    c_entidad.obtener_datos();
                    cbx_moneda.setEnabled(true);
                    cbx_moneda.requestFocus();
                    txt_razon_social.setText(c_entidad.getNombre());
                } else {
                    Frame f = JOptionPane.getRootFrame();
                    frm_reg_entidad dialog = new frm_reg_entidad(f, true);
                    dialog.setLocationRelativeTo(null);
                    dialog.setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_txt_proveedorKeyPressed

